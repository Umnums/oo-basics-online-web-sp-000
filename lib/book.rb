class Book
  def initialize(book)
    @title = book
  end

  def title
    @title
  end

  def author=(author)
    @author = author
  end

  def author
    @author
  end

  def pagecount=(pagecount)
    @pagecount = pagecount
  end

  def pagecount
    @pagecount
  end

  def genre=(genre)
    @genre = genre
  end

  def genre
    @genre
  end

end
